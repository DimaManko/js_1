function capitalizeWords(str) {
  let result = ''; // —Å—Ç—Ä–æ–∫–∞, –≤ –∫–æ—Ç–æ—Ä—É—é –±—É–¥–µ—Ç –∑–∞–ø–∏—Å–∞–Ω —Ä–µ–∑—É–ª—å—Ç–∞—Ç.
  let capitalizeNext = true; // —Ñ–ª–∞–≥, –∫–æ—Ç–æ—Ä—ã–π —É–∫–∞–∑—ã–≤–∞–µ—Ç, —á—Ç–æ —Å–ª–µ–¥—É—é—â–∏–π –Ω–µ–ø—Ä–æ–±–µ–ª—å–Ω—ã–π —Å–∏–º–≤–æ–ª –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –∑–∞–≥–ª–∞–≤–Ω—ã–º.

  for (let i = 0; i < str.length; i++) {
    const char = str[i];
    // –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª ‚Äî –ø—Ä–æ–±–µ–ª (' '), –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ result –∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º capitalizeNext = true.
    if (char === ' ') {
      capitalizeNext = true; 
      result += ' ';
    } 
    // –ï—Å–ª–∏ capitalizeNext === true, –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ (toUpperCase()) –∏ —Å–±—Ä–∞—Å—ã–≤–∞–µ–º —Ñ–ª–∞–≥.
      else { 
      if (capitalizeNext) {
        result += char.toUpperCase();
        capitalizeNext = false;
      } // –ò–Ω–∞—á–µ –¥–æ–±–∞–≤–ª—è–µ–º —Å–∏–º–≤–æ–ª –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ (toLowerCase()).
       else {
        result += char.toLowerCase();
      }
    }
  }

  return result;
}

